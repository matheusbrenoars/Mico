class Triangulo:
    def __init__(self, v0, v1, v2):
        self.v0 = v0
        self.v1 = v1
        self.v2 = v2
        
    def __str__(self):
        return f"[{self.v0}, {self.v1}, {self.v2}]"
        
    
    
class Quadrado:
    def __init__(self, v0, v1, v2, v3):
        self.v0 = v0
        self.v1 = v1
        self.v2 = v2
        self.v3 = v3

   
